package example;

public class _2_7DataType {
	public static void main(String[] args) {
		
		System.out.println("7/2 = " + 7 / 2); // 7/2 정수값으로 계산
		System.out.println("7/2.0 = " + 7 / 2.0); // 7/2.0 소수점값으로 계산
		System.out.println("7.0/2 = " + 7.0 / 2); // 7.0/2 소수점값으로 계산
		System.out.println("7.0/2.0 = " + 7.0 / 2.0); // 7.0/2.0 소수점값으로 계산
	}
}

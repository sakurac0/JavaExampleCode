package example;

public class _2_4Opertaor3 {
public static void main(String[] args) {
	int a = 10,b = 10;
	System.out.println(++a); // 변수 a값에 +1먼저하고 출력
	System.out.println(b++); // 변수b값을 먼저 출력하고 +1
	System.out.println(--a); // 변수 a값에 -1 먼저하고 출력
	System.out.println(b--); // 변수 b값을 먼저 출력하고-1 
}
}

package example;

public class _2_6Opertaor5 {
	public static void main(String[] args) {
		int a = 10, b = 10;
		System.out.println((a == 10) && (b == 5)); // a==10 and b==5 false
		System.out.println((a == 10) || (b == 5)); // a==10 or b==5 true
		System.out.println(!(a == 10)); // a!=10 false
	}
}

package example;

public class _2_2Operator {
	public static void main(String[] args) {
		
		System.out.println("8+3=" + (8 + 3)); // 8+3 값출력
		System.out.println("9-2=" + (9 - 2)); // 9-2 값출력
		System.out.println("10*5=" + (10 * 5)); // 10*5 값출력
		System.out.println("8/2=" + (8 / 2)); // 8/2 값출력
		System.out.println("9/2의 나머지 = " + (9 % 2)); // 9/2의 나머지값 출력
	}
}
